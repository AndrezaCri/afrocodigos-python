### NUMBER ###
integerNumberPositive = 2
integerNumberNegative = -2
floatNumber = 2.0
print(type(integerNumberNegative))
print(integerNumberNegative, integerNumberPositive, floatNumber)
print("-"*100)

# ### BOOLEAN ###
male = False
female = True
print(type(male))
print(female)
print("-"*100)

# ### STRINGS ###
print(type(chr(97)))
print(type("oi"))
print(type('oi'))
print(type('2'))
print("Felipe falou: 'Olá! tudo bem?' e ninguém o respondeu.")
print("-"*100)

# ### Lista ###
numbersList = [-5,0,3,9.4,10.000]
print(type(numbersList))
print(numbersList)
print("-"*100)

# ### Tuple ###
print(type((1,2,3,4,5)))
print("-"*100)

# ### Conjunto (set) ###
print(type({1,2,3,4,5}))
print("-"*100)

# ### Dicionario (dict) ###
print(type({"numero": 2}))

print(
    {
        "aluno": {
            "idade": 10,
            "nome": "Joãozinho",
        }
    }
)
print("-"*100)

